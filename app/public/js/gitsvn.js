function validateForm()
{
    console.log("in validateForm()");
    var errors = false;
    $(".error").html("");
    
    var items = ["svndir", "githuburl", "email"];
    for (var i = 0; i < items.length; i++)
    {
        var sel = "#" + items[i];
        var err = sel + "_error";
        if ($(sel).val().trim() == "")
        {
            console.log("yes, " + items[i] + " is blank");
            errors = true;
            $(err).html("Can't be blank");
        }
    }

    if (errors)
        return(false);

    if (!validateEmail($("#email").val()))
    {
        errors = true;
        $("#email_error").html("Not a valid email");
    }

    if (!validateUrl($("#githuburl").val()))
    {
        errors = true;
        $("#githuburl_error").html("Not a valid URL")
    }


    if (errors)
        return(false);
    else
        return(true);
}